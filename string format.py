# slice method in python strings
x="Hello Wlord"
print("x=",x)
print("the length of a string =",len(x))
print("x[2:7]=",x[2:7])
print("x[:7]=",x[:7])
print("x[2:]=",x[2:])
print("x[:]=",x[:])
print("x[-7:-2]=",x[-7:-2])
print("x[-8:-4]=",x[-8:-4])
print(x.upper())
print(x.lower())
#replace a word
print(x.replace("Hello","python"))